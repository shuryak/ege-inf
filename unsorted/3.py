N = int(input())

k1 = k2 = k7 = k14 = 0

for i in range(N):
    x = int(input())

    if x % 14 == 0:
        k14 += 1
    elif x % 7 == 0:
        k7 += 1
    elif x % 2 == 0:
        k2 += 1
    else:
        k1 += 1

# Количество всех пар, произведение элементов которых кратно 14:
M = (k14 * (k1 + k2 + k7)) + (k2 * k7) + (k14 * (k14 - 1) // 2)

# (N * (N - 1) // 2) - количество всех возможных пар
result = (N * (N - 1) // 2) - M

print(result)
